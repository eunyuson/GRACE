// Get unique categories for tag filtering
export const getCategories = (): string[] => {
    const cats = new Set(hymnData.map(h => h.category));
    return Array.from(cats).sort();
};

// Search hymns by number, title, code, or category
export const searchHymns = (query: string, categoryFilter?: string): HymnInfo[] => {
    let results = hymnData;

    if (categoryFilter) {
        results = results.filter(h => h.category === categoryFilter);
    }

    if (!query) return results;

    const q = query.toLowerCase();
    return results.filter(h =>
        h.number.toString().startsWith(q) ||
        h.title.toLowerCase().includes(q) ||
        h.code.toLowerCase() === q ||
        h.category.toLowerCase().includes(q)
    );
};
