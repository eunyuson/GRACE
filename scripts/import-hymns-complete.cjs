async function main() {
    console.log('üéµ Starting Hymn Import to Firestore...\n');
    console.log(`üìä Total hymns in this batch: ${Object.keys(hymnFileIds).length}`);

    // Check existing hymns
    const existingSnapshot = await db.collection('gallery')
        .where('type', '==', 'hymn')
        .get();

    const existingNumbers = new Set();
    existingSnapshot.docs.forEach(doc => {
        const data = doc.data();
        if (data.number) existingNumbers.add(data.number);
    });

    console.log(`üìä Existing hymns in DB: ${existingNumbers.size}`);

    // Process hymns
    let added = 0;
    let skipped = 0;
    let failed = 0;

    const hymnNumbers = Object.keys(hymnFileIds).map(Number).sort((a, b) => a - b);

    for (const num of hymnNumbers) {
        const fileId = hymnFileIds[num.toString()];

        if (existingNumbers.has(num)) {
            skipped++;
            continue;
        }

        try {
            // Use lh3.googleusercontent.com for direct image access
            const imageUrl = `https://lh3.googleusercontent.com/d/${fileId}`;

            await db.collection('gallery').add({
                type: 'hymn',
                number: num,
                title: `Ï∞¨ÏÜ°Í∞Ä ${num}Ïû•`,
                imageUrl: imageUrl,
                lyrics: '',
                youtubeLinks: [],
                createdAt: admin.firestore.FieldValue.serverTimestamp(),
                source: 'google-drive',
                driveFileId: fileId
            });

            added++;
            process.stdout.write(`\r   Processing: ${added + skipped}/${hymnNumbers.length} (Added: ${added}, Skipped: ${skipped})`);
        } catch (error) {
            failed++;
            console.error(`\n‚ùå Error adding hymn ${num}: ${error.message}`);
        }
    }

    console.log('\n');
    console.log('='.repeat(50));
    console.log(`‚úÖ Successfully added: ${added} hymns`);
    console.log(`‚è≠Ô∏è  Skipped (already exist): ${skipped} hymns`);
    if (failed > 0) {
        console.log(`‚ùå Failed: ${failed} hymns`);
    }
    console.log('='.repeat(50));

    process.exit(0);
}

